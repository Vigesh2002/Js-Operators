// Arithmetic Operators

// Addition

let a = 10 + 5;
document.getElementById("one").innerHTML = a;

// A typical arithmetic operation takes two numbers (or variables) and produces a new number.
let b = 100;
let c = 100;
let x = b + c;
document.getElementById("one_one").innerHTML = x;

// Subtraction

let d = 10 - 5;
document.getElementById("two").innerHTML = d;

let e = 100;
let f = 50;
let g = e - f;
document.getElementById("two_two").innerHTML = g;

// Multiplication

let h = 10 * 5;
document.getElementById("three").innerHTML = h;

let i = 5;
let j = 2;
let k = i * j;
document.getElementById("three_three").innerHTML = k;

// Exponentiation

let y = 5;
let z = y ** 2;
document.getElementById("four").innerHTML = z;

// Division

let l = 10 / 5;
document.getElementById("five").innerHTML = l;

let m = 100;
let n = 50;
let o = m / n;

document.getElementById("five_five").innerHTML = o;

// Modulus

let p = 10 % 5;
document.getElementById("six").innerHTML = p;

let q = 10;
let r = 5;
let s = q % r;
document.getElementById("six_six").innerHTML = s;


// Increment

let t = 5;
t++;
let u = t;
document.getElementById("seven").innerHTML = u;

// Decrement

let v = 5;
v--;
let w = v;
document.getElementById("eight").innerHTML = w;